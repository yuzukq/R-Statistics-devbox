# ============================================================
#  検定力分析スクリプト
#  研究題目：擬似触覚付加による電気的筋肉刺激強度の低減可能性の検討
#  実験設計：被験者内1要因（EMS×PH条件）、5条件 or 4条件
# ============================================================

library(pwr)

cat("============================================================\n")
cat(" 検定力分析：5条件 vs 4条件\n")
cat("============================================================\n\n")


# ============================================================
# PART 1：全体の分散分析（F検定）の検定力
# ============================================================
# pwr.anova.test() は一元配置分散分析の検定力を計算する。
# 本来は被験者間設計の計算式だが、被験者内設計の近似として使える。
# ただし被験者内設計では条件間の相関のぶん誤差が小さくなるため、
# 実際の検定力はここで出る値より高くなる（PART 2 で補正する）。
#
# 引数：
#   k        = 条件数（水準数）
#   n        = 各条件の人数（被験者数）
#   f        = 効果量 f（小=0.10, 中=0.25, 大=0.40）
#   sig.level = 有意水準 α
#   power    = 検定力（これを求める場合は省略）

cat("--------------------------------------------------------------\n")
cat(" PART 1：F検定の検定力（被験者間近似 / α=0.05）\n")
cat(" ※ 被験者内設計の実際の値はこれより高い（PART 2 参照）\n")
cat("--------------------------------------------------------------\n")
cat(sprintf(
  "%-6s %-5s %10s %10s %10s\n",
  "条件数",
  "n",
  "小(f=.10)",
  "中(f=.25)",
  "大(f=.40)"
))
cat(strrep("-", 45), "\n")

for (k in c(5, 4)) {
  for (n in c(15, 18, 20, 25, 30)) {
    ps <- pwr.anova.test(k = k, n = n, f = 0.10, sig.level = 0.05)$power
    pm <- pwr.anova.test(k = k, n = n, f = 0.25, sig.level = 0.05)$power
    pl <- pwr.anova.test(k = k, n = n, f = 0.40, sig.level = 0.05)$power
    cat(sprintf("k=%-3d   n=%-4d %10.3f %10.3f %10.3f\n", k, n, ps, pm, pl))
  }
  cat("\n")
}

cat("\n【検定力0.80に必要な最小サンプルサイズ（被験者間近似）】\n")
for (k in c(5, 4)) {
  rs <- pwr.anova.test(k = k, f = 0.10, sig.level = 0.05, power = 0.80)
  rm <- pwr.anova.test(k = k, f = 0.25, sig.level = 0.05, power = 0.80)
  rl <- pwr.anova.test(k = k, f = 0.40, sig.level = 0.05, power = 0.80)
  cat(sprintf(
    "k=%d: 小効果→%d人, 中効果→%d人, 大効果→%d人\n",
    k,
    ceiling(rs$n),
    ceiling(rm$n),
    ceiling(rl$n)
  ))
}


# ============================================================
# PART 2：反復測定補正版（条件間相関ρを考慮）
# ============================================================
# 被験者内（反復測定）設計では、同じ参加者が全条件を体験するため
# 個人差による変動が誤差から除かれ、検定力が上がる。
#
# 補正式：f_eff = f / sqrt(1 - ρ)
#   ρ（ロー）= 条件間の相関係数（同じ人が違う条件でどれくらい似た反応をするか）
#
# ρ の目安：
#   ρ = 0.5 → 中程度の相関（保守的な見積もり）
#   ρ = 0.7 → 高い相関（個人差が大きい場合によくある）

cat("\n\n--------------------------------------------------------------\n")
cat(" PART 2：反復測定補正版（条件間相関ρを考慮）\n")
cat(" 補正式: f_eff = f / sqrt(1 - ρ)\n")
cat("--------------------------------------------------------------\n")

for (rho in c(0.5, 0.7)) {
  f_adj <- 1 / sqrt(1 - rho)
  cat(sprintf(
    "\n【ρ = %.1f のとき：効果量 f は %.2f 倍として扱える】\n",
    rho,
    f_adj
  ))
  cat(sprintf(
    "%-6s %-5s %10s %10s %10s\n",
    "条件数",
    "n",
    "小(f=.10)",
    "中(f=.25)",
    "大(f=.40)"
  ))
  cat(strrep("-", 45), "\n")

  for (k in c(5, 4)) {
    for (n in c(15, 18, 20)) {
      ps <- pwr.anova.test(
        k = k,
        n = n,
        f = 0.10 * f_adj,
        sig.level = 0.05
      )$power
      pm <- pwr.anova.test(
        k = k,
        n = n,
        f = 0.25 * f_adj,
        sig.level = 0.05
      )$power
      pl <- pwr.anova.test(
        k = k,
        n = n,
        f = 0.40 * f_adj,
        sig.level = 0.05
      )$power
      cat(sprintf("k=%-3d   n=%-4d %10.3f %10.3f %10.3f\n", k, n, ps, pm, pl))
    }
    cat("\n")
  }

  cat(sprintf("  検定力0.80に必要なn（ρ=%.1f）:\n", rho))
  for (k in c(5, 4)) {
    rm <- pwr.anova.test(
      k = k,
      f = 0.25 * f_adj,
      sig.level = 0.05,
      power = 0.80
    )
    rl <- pwr.anova.test(
      k = k,
      f = 0.40 * f_adj,
      sig.level = 0.05,
      power = 0.80
    )
    cat(sprintf(
      "    k=%d: 中効果→%d人, 大効果→%d人\n",
      k,
      ceiling(rm$n),
      ceiling(rl$n)
    ))
  }
}


# ============================================================
# PART 3：多重比較（Bonferroni補正）後の検定力
# ============================================================
# F検定で有意になっても、どの条件間に差があるかは事後検定（多重比較）で調べる。
# Bonferroni補正では α を比較回数で割るため、
# 条件数が多いほど各比較の有意水準が厳しくなり、検定力が落ちる。
#
# 例：5条件 → C(5,2)=10ペア → 各比較のα = 0.05/10 = 0.005
#     4条件 → C(4,2)= 6ペア → 各比較のα = 0.05/ 6 = 0.0083
#
# pwr.t.test(type="paired") = 対応ありt検定の検定力
#   d = Cohen's d（小=0.20, 中=0.50, 大=0.80）
#   n = 被験者数
#   sig.level = Bonferroni補正後のα

cat("\n\n--------------------------------------------------------------\n")
cat(" PART 3：多重比較（Bonferroni補正）後の各ペアの検定力\n")
cat(" 対応ありt検定, d = 0.5（中効果量）\n")
cat("--------------------------------------------------------------\n")
cat(sprintf(
  "%-4s %6s %10s %8s %8s %8s\n",
  "k",
  "ペア数",
  "Bonf. α",
  "n=15",
  "n=18",
  "n=20"
))
cat(strrep("-", 50), "\n")

for (k in c(5, 4)) {
  n_pairs <- k * (k - 1) / 2
  alpha_b <- 0.05 / n_pairs
  p15 <- pwr.t.test(n = 15, d = 0.5, sig.level = alpha_b, type = "paired")$power
  p18 <- pwr.t.test(n = 18, d = 0.5, sig.level = alpha_b, type = "paired")$power
  p20 <- pwr.t.test(n = 20, d = 0.5, sig.level = alpha_b, type = "paired")$power
  cat(sprintf(
    "%-4d %6d %10.4f %8.3f %8.3f %8.3f\n",
    k,
    n_pairs,
    alpha_b,
    p15,
    p18,
    p20
  ))
}

cat("\n【計画的比較：仮説に対応するペアだけ検定する場合】\n")
cat("  理論的に必須な比較：①vs②, ①vs③, ②vs④（研究仮説に直結する3ペア）\n\n")
cat(sprintf(
  "%-10s %8s %8s %8s %8s\n",
  "比較ペア数",
  "α",
  "n=15",
  "n=18",
  "n=20"
))
cat(strrep("-", 45), "\n")

for (np in c(3, 4, 6, 10)) {
  ap <- 0.05 / np
  p15 <- pwr.t.test(n = 15, d = 0.5, sig.level = ap, type = "paired")$power
  p18 <- pwr.t.test(n = 18, d = 0.5, sig.level = ap, type = "paired")$power
  p20 <- pwr.t.test(n = 20, d = 0.5, sig.level = ap, type = "paired")$power
  label <- ifelse(
    np == 3,
    "3ペア（推奨）",
    ifelse(np == 4, "4ペア", ifelse(np == 6, "6ペア(k=4全)", "10ペア(k=5全)"))
  )
  cat(sprintf("%-12s %8.4f %8.3f %8.3f %8.3f\n", label, ap, p15, p18, p20))
}

cat("\n\n--------------------------------------------------------------\n")
cat(" まとめ\n")
cat("--------------------------------------------------------------\n")
cat("・全体F検定：反復測定ρ=0.5想定なら、5条件n=20で検定力≈0.80（問題なし）\n")
cat("・5→4条件削減：F検定の改善効果は小さく、ペア比較も0.21→0.27程度\n")
cat("・本質的な課題：多重比較補正後の各ペア検定力が低い（0.2〜0.3台）\n")
cat(
  "・有効な対処：比較を計画的3ペアに絞る、またはタスクを工夫して効果量を上げる\n"
)
