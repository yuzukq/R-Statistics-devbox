# 課題5のデータを2回繰り返した見かけ上10人のデータについて、
# 相関係数がゼロであるかを無相関検定により確認しなさい。

df <- read.csv("../../data/exercise/ex6.csv")
kokugo2 <- df$国語
shakai2 <- df$社会

cor.test(kokugo2, shakai2, method = "pearson")

# --- 結果の解釈 ---
# 課題5と相関係数 r の値は同じだが、n = 10（df = 8）になったことで
# t 統計量が大きくなり p値 < 0.05 となり帰無仮説を棄却できる。
#
# 同じ相関の強さでも、サンプルサイズが大きいほど有意になりやすい。
# これは標本サイズが検定力（power）に直結していることを示している。
#
# ただし今回のデータは同じ5人を単純に2回繰り返したもので、
# 実際には独立したデータではないため、結果の解釈には注意が必要。
