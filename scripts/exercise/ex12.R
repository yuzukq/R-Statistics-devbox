# 検定力分析によるサンプルサイズの決定
# power.t.test() の4つの引数のうち1つを NULL にすると、その値を逆算してくれる。
#   n      : サンプルサイズ（各群）
#   delta  : 検出したい平均の差
#   sd     : 標準偏差
#   sig.level : 有意水準（デフォルト 0.05）
#   power  : 検定力（デフォルト 0.80）

# --- (1) サンプルサイズを求める ---
# 差 delta=5, sd=10, power=0.80 を達成するのに必要な n は？
power.t.test(delta = 5, sd = 10, sig.level = 0.05, power = 0.80)

# --- (2) 検定力を求める ---
# n=20 のとき、同じ条件での検定力は？
power.t.test(n = 20, delta = 5, sd = 10, sig.level = 0.05)

# --- (3) 検出できる最小の差を求める ---
# n=20, power=0.80 のとき、検出できる delta は？
power.t.test(n = 20, sd = 10, sig.level = 0.05, power = 0.80)

# --- 結果の解釈 ---
# 検定力分析の4要素はトレードオフの関係にある：
#   - n を増やす → 検定力が上がる・小さな差も検出できる
#   - delta を大きくする（大きな差を検出したい）→ 必要な n が減る
#   - sig.level を厳しくする（0.01 など）→ 必要な n が増える
#   - power を高くする（0.90 など）→ 必要な n が増える
#
# 事前に power=0.80 を目標にサンプルサイズを設計することで、
# 「集めたデータが少なすぎて有意にならなかった」を防げる。
