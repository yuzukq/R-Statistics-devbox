# 課題7と同じデータを用いて、
# 心理学テストの得点に男女で有意な差があるかを両側検定で確認しなさい。

df <- read.csv("../../data/exercise/ex7.csv")

psych_male <- df$心理学テスト[df$性別 == "男"]
psych_female <- df$心理学テスト[df$性別 == "女"]

t.test(psych_male, psych_female, alternative = "two.sided")

# --- 結果の解釈 ---
# 帰無仮説：男女の心理学テスト得点の母平均は等しい
# 対立仮説：母平均は等しくない（両側）
#
# p値: t統計量以上に極端な値が出る確率
# 男女各10人で平均点の差が小さいため、p値 > 0.05 となり
# 帰無仮説を棄却できない（有意な差なし）と予想される。
#
# 課題7（統計の好みによる統計テストの差）と比べると、
# 群の分け方・比較する変数の組み合わせによって結論が変わることがわかる。
