# 課題2と同じデータに対し、スピアマンの順位相関係数およびケンドールの順位相関係数が
# それぞれゼロであるかを、無相関検定により確認しなさい。

df <- read.csv("../../data/exercise/ex2.csv")
study_time <- df$勉強時間
test_score <- df$定期試験の得点

# スピアマンの順位相関係数
cor.test(study_time, test_score, method = "spearman")

# ケンドールの順位相関係数
cor.test(study_time, test_score, method = "kendall")


# ピアソンとスピアマンの違い
# スピアマンは値を順位に変換してからピアソンと同じ計算をします。
# 今回は「勉強12時間→100点」のような大きな値の学
# 生がいますが、順位に直すと外れ値的な影響が薄まるため、
# スピアマンの方がわずかに高くなっています。

