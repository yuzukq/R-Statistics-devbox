# 大学生10人の一日の勉強時間（単位：時間）と定期試験の得点（100点満点）のデータを以下に示す。
# 勉強時間と定期試験の得点との間の相関係数（ピアソンの積率相関係数）がゼロであるかを、無相関検定により確認しなさい。


# データ読み込み
data <- read.csv("../../data/exercise/ex2.csv")
study_time <- data$勉強時間
test_score <- data$定期試験の得点

# 無相関検定（ピアソン）
cor.test(study_time, test_score, method = "pearson")


# p値 = 0.000265 < 0.05（有意水準）なので、帰無仮説「相関係数 = 0」を棄却。勉強時間と試験得点の間には有意な相関がある。
