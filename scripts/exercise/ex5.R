# 生徒5人の国語と社会の試験の得点について、
# 相関係数がゼロであるかを無相関検定により確認しなさい。

df <- read.csv("../../data/exercise/ex5.csv")
kokugo <- df$国語
shakai <- df$社会

cor.test(kokugo, shakai, method = "pearson")

# --- 結果の解釈 ---
# r ≈ 0.84 と相関係数自体は大きいが、
# n = 5（df = 3）という小標本のため t 統計量が臨界値に届かず、
# p値 > 0.05 となり帰無仮説を棄却できない。
#
# 「相関がなさそう」ではなく「標本が小さすぎて判断できない」という結論。
