# 統計が「好き」か「嫌い」かで2群に分けたとき、
# 統計テストの得点に有意な差があるかを両側検定で確認しなさい。

df <- read.csv("../../data/exercise/ex7.csv")

stat_like    <- df$統計テスト[df$統計の好み == "好"]
stat_dislike <- df$統計テスト[df$統計の好み == "嫌"]

t.test(stat_like, stat_dislike, alternative = "two.sided")

# --- 結果の解釈 ---
# 帰無仮説：「好き」群と「嫌い」群の統計テスト得点の母平均は等しい
# 対立仮説：母平均は等しくない（両側）
#
# p値 < 0.05 であれば2群間に有意な差があると結論づける。
# R の t.test() はデフォルトでウェルチのt検定（分散が等しいことを仮定しない）を使う。
